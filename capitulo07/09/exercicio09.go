// Crie um programa que utilize a declaração switch, sem switch statement especificado.
package main

import "fmt"

func main() {
	esporteFavorito := "Dormir"
	switch esporteFavorito {
	case "Dormir":
		fmt.Println("zzzzzz")
	case "Volei":
		fmt.Println("Volei")
	case "Futebol":
		fmt.Println("Futebol")

	}

}
